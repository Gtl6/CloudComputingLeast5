public class StringMovieCombiner {
	Long myLong;
	String myString;
	
	public StringMovieCombiner() {};
	public StringMovieCombiner(Long along, String astring) {
		myLong = along;
		myString = astring;
	}
	
	public Long getLong() { return myLong; }
	
	public String getString() { return myString; }
}
